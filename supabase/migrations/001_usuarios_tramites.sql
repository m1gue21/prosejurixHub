create policy "comunicaciones_all_anon" on public.comunicaciones for all using (true) with check (true);
create policy "agenda_notas_all_anon" on public.agenda_notas for all using (true) with check (true);
create policy "profiles_select_own" on public.profiles for select using (auth.uid() = id);

